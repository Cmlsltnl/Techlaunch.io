            		<td><b><u>Term</u></b>. This Agreement shall be for a term of six (6) months (the “<u>Initial Term</u>”). The Initial Term shall be automatically renewed for successive six (6) month periods (the “<u>Renewal Term</u>” and together with the Initial Term, the “<u>Term</u>”). Either party may terminate this Agreement at any time by giving ten (10) days’ notice to the other party.</td>
