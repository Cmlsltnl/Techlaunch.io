            		<td style="width: 0.25in">1.</td>
            		<td><b><u>Engagement</u></b>. Subject to the terms and conditions of this Agreement, Service Provider hereby agrees to provide the Company with the services of those employees of Employer listed on <u>Schedule A</u> attached hereto, as may be modified from time to time (the "<u>Leased Employees</u>"). The services to be performed hereunder by the Leased Employees shall be the same as, or substantially similar to, the services performed by the Leased Employees on behalf of Employer immediately prior to the date hereof, unless otherwise directed by the Service Provider and the Company.</td>
