            	<b>&nbsp;</b>
            </p>
            <p style="margin: 0pt 0; text-align: justify; text-indent: 0.5in">
            	<b>WHEREAS</b>, the Parties desire to enter into this Agreement in order to set forth the terms of their agreement regarding the Company's utilization of the Service Provider's employees in the Business.
            </p>
            <br>
            <p style="margin: 0pt 0; text-align: justify; text-indent: 0.5in">
